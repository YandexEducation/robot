"""Короткий пример чтения Sharp.

Sharp даёт «сырое» значение АЦП и оценку расстояния в сантиметрах.
"""


def run_robot(bot):
    # Бесконечный цикл опроса Sharp (датчик всегда подключён)
    while True:
        # Считываем сырое значение АЦП
        raw = bot.sharp.read()
        # Считываем оценку расстояния в сантиметрах
        dist_cm = bot.sharp.distance_cm()
        # Отправляем оба значения в лог веб‑интерфейса
        bot.log("Sharp raw={} dist_cm={}".format(raw, dist_cm))

        # Пауза 0.5 секунды между измерениями
        yield bot.sleep(0.5)

