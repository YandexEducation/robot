"""Короткий пример чтения датчиков линии.

Три датчика: левый, центральный и правый.
"""


def run_robot(bot):
    # Бесконечный цикл опроса датчиков
    while True:
        # Считываем левый датчик линии (аналоговое значение 0..4095)
        left = bot.line_left.read()
        # Считываем центральный датчик линии
        center = bot.line_sensor.read()
        # Считываем правый датчик линии
        right = bot.line_right.read()

        # Отправляем три значения в лог веб‑интерфейса
        bot.log("Line L/C/R: {} {} {}".format(left, center, right))

        # Небольшая пауза между измерениями
        yield bot.sleep(0.2)

