"""Короткий пример чтения энкодеров.

Показывает, как получить количество «щелчков» с левого и правого колёс.
"""


def run_robot(bot):
    # Бесконечный цикл: каждые 0.5 секунды читаем и выводим значения
    while True:
        # Считываем значение левого энкодера
        left = bot.left_encoder.read()
        # Считываем значение правого энкодера
        right = bot.right_encoder.read()
        # Отправляем строку в лог веб‑интерфейса (видно в окне «Лог отладки»)
        bot.log("Encoders L/R: {} {}".format(left, right))
        # Делаем паузу 0.5 секунды, чтобы не спамить лог слишком часто
        yield bot.sleep(0.5)

