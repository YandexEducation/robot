"""Короткий пример чтения камеры.

Камера возвращает класс знака и уверенность модели в процентах.
"""


def run_robot(bot):
    # Бесконечный цикл опроса камеры
    while True:
        # Считываем класс (sign) и уверенность (conf, 0..100)
        sign, conf = bot.camera.detect_sign_with_conf()
        # Отправляем результат в лог веб‑интерфейса
        bot.log("Camera sign={} conf={}".format(sign, conf))
        # Небольшая пауза между запросами к камере
        yield bot.sleep(0.2)

